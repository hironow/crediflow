	<h1>About</h1>

	<p>
		Crediflow combines "Credit" and "Flow" to provide a decentralized platform for creators and
		admirers to share and reward each other's content.
	</p>

	<p>
		This contract is for Crediflow, a decentralized platform on the Flow blockchain that enables
		creators and admirers to share and reward each other with end credits and tips.
	</p>

	<p>This app shows how to:</p>
	<ul>
		<li>Anyone can mint NFTs as an Admirer and send tips.</li>
		<li>As a Creator registered on Crediflow, one can mint NFTs and claim them.</li>
		<li>It is possible to create one's own Crediflow as a host.</li>
	</ul>

	<p>
		The main purpose of Crediflow is to provide a platform for creators to receive recognition for
		their work and for admirers to show their appreciation and support through tipping.
